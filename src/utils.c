void	*_realloc(
			void *p,
			size_t oldsize,
			size_t newsize
			)
{
	char	*newp;

	newp = malloc(newsize);
	if (newp == NULL)
	{
		free(p);
		return (NULL);
	}
	ft_memcpy(newp, p, oldsize);
	free(p);
	return (newp);
}
