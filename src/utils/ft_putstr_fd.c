#include <stdlib.h>
#include <unistd.h>

// ************************************************************************** //
// *                                                                        * //
// * Header function.                                                       * //
// *                                                                        * //
// ************************************************************************** //

void	ft_putstr_fd(
			char *s,
			int fd
			)
{
	if (s != NULL)
		while (*s != '\0')
			write(fd, s++, 1);
}
